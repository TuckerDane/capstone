#include "Room.h"

/***************************************************************************************************
**	Room constuctor
***************************************************************************************************/
Room::Room()
{
	setSpSize();
}

/***************************************************************************************************
**	sets size of room
***************************************************************************************************/
void Room::setSpSize()
{
	spSizeRow = 11;
	spSizeCol = 11;
}

/***************************************************************************************************
**	sets the room up
***************************************************************************************************/
void Room::setSpace(string sn, Player *p, Space *l, Space *r, Space *t, Space *b, Object *o, int num)
{
	spaceName = sn;
	spaceNum = num;
	spaceType = "SMALL ROOM";
	setObjectPos(o);
	player = p;
	setSpSize();
	setDoors(!(l == NULL), !(r == NULL), !(t == NULL), !(b == NULL));
	left = l;
	right = r;
	top = t;
	bottom = b;
}

/***************************************************************************************************
**	Sets sone position
***************************************************************************************************/
void Room::setObjectPos(Object *o)
{
	this->setSpPos(o, o->getRowPos(), o->getColPos());
}
