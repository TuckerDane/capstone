#include "Door.h"

/***************************************************************************************************
**	default constructor for Door (sets it to null)
***************************************************************************************************/
Door::Door() : Object(' ', "Generic", -1, -1, "Door")
{
	weight = 100;
}

/***************************************************************************************************
**	constructor for Door initializing rowPos and colPos
***************************************************************************************************/
Door::Door(char s, int row, int col) : Object(s, "Generic", row, col, "Door")
{
}

/***************************************************************************************************
**	deconstructor for Door
***************************************************************************************************/
Door::~Door()
{
}

/***************************************************************************************************
**	a function which changes the Door's symbol
***************************************************************************************************/
void Door::setSymbol(char s)
{
	symbol = s;
}

/***************************************************************************************************
**	a function which changes the Door's status
***************************************************************************************************/
void Door::setOpen(bool o)
{
	open = o;
}

/***************************************************************************************************
**	returns the weight of the object
***************************************************************************************************/
int Door::getWeight()
{
	return weight;
}

/***************************************************************************************************
**	returns true if the door is open
***************************************************************************************************/
bool Door::getOpen()
{
	return open;
}

/***************************************************************************************************
**	returns the name of the Object
***************************************************************************************************/
string Door::getName()
{
	return name;
}

/***************************************************************************************************
**	returns the weight of the object
***************************************************************************************************/
void Door::setWeight(int w)
{
	weight = w;
}